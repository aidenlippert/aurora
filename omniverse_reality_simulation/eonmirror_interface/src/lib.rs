#[derive(Debug, Clone)]
pub struct RealWorldDataPoint {
    pub source_id: String,       // e.g., "iot_sensor_zoneA_temp", "api_financial_feed_XYZ"
    pub data_type: String,       // e.g., "temperature", "pollution_ppm", "stock_price"
    pub value_as_string: String, // Keep it simple for mock
    pub timestamp: u64,
    pub location_geohash: Option<String>, // Optional location
    pub metadata: HashMap<String, String>, // e.g., units, sensor_model
}

// This function simulates an oracle or data feed ingesting data.
// In reality, TruthOracles (Section 4.1.1) would be more complex with ZKPs.
pub fn ingest_real_world_data(
    data_point: RealWorldDataPoint,
    current_block_height: u64,
) -> Result<IsnNode, String> {
    println!(
        "[EonMirror] Ingesting real-world data from Source ID: '{}', Type: '{}', Value: '{}'",
        data_point.source_id, data_point.data_type, data_point.value_as_string
    );

    // Prepare details for ISN recording
    let mut details = data_point.metadata.clone();
    details.insert("source_id".to_string(), data_point.source_id.clone());
    details.insert("data_type".to_string(), data_point.data_type.clone());
    details.insert("value".to_string(), data_point.value_as_string.clone());
    details.insert("timestamp".to_string(), data_point.timestamp.to_string());
    if let Some(ref loc) = data_point.location_geohash {
        details.insert("location".to_string(), loc.clone());
    }

    // Record in ISN via Cosmic Data Constellation
    // The ID for the ISN node will be generated by record_real_world_data_point
    match record_real_world_data_point(
        &data_point.source_id, // Use source_id as part of subject for ISN
        &data_point.data_type,
        current_block_height,
        details,
    ) {
        Ok(isn_node) => {
            println!("[EonMirror] Data point from Source '{}' recorded in ISN. Node ID: {}",
                data_point.source_id, isn_node.id);
            Ok(isn_node)
        }
        Err(e) => {
            eprintln!("[EonMirror] Error recording data point from Source '{}' in ISN: {}",
                data_point.source_id, e);
            Err(e)
        }
    }
}

